declare interface Array<T> {
  unique(): T[];
}

// make the Array object smarter
if (!Array.prototype.unique) {
  Array.prototype.unique = function(): any[] {
    return this.filter((value, index: number, self: any[]) => self.indexOf(value) === index);
  };
}

declare interface NumberConstructor {
  parseRoman(roman: string): number;
}

Number.parseRoman =
  Number.parseRoman ||
  (() => {
    const map = { I: 1, V: 5, X: 10, L: 50, C: 100, D: 500, M: 1000 };
    const charToInt = (char: string): number => (map.hasOwnProperty(char) ? map[char] : Number.NaN);
    return (str: string): number => {
      if (str) {
        try {
          let pre = null;
          const value: number = str
            .toUpperCase()
            .split('')
            .reduce((a: number, e: string) => {
              const curr = charToInt(e);
              let r = curr;
              if (a > 0 && curr > pre) {
                r = curr - pre * 2;
              }
              pre = curr;
              return a + r;
            }, 0);

          return value;
        } catch (e) {
          // return Number.NaN
        }
      }

      return Number.NaN;
    };
  })();

declare interface Number {
  toRoman(): string;
}

Number.prototype.toRoman =
  Number.prototype.toRoman ||
  (function() {
    const decimal = [1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1];
    const roman = {
      1: 'I',
      4: 'IV',
      5: 'V',
      9: 'IX',
      10: 'X',
      40: 'XL',
      50: 'L',
      90: 'XC',
      100: 'C',
      400: 'CD',
      500: 'D',
      900: 'CM',
      1000: 'M',
    };

    return function(): string {
      let int: number = this;
      let str = '';
      decimal.forEach((e) => {
        while (int % e < int) {
          str += roman[e];
          int -= e;
        }
      });
      return str;
    };
  })();

